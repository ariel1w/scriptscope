'use client';

interface ResultsProps {
  analysis: any;
  scriptId: string;
}

export default function Results({ analysis, scriptId }: ResultsProps) {
  const page1 = analysis.page1_overview || {};
  const page2 = analysis.page2_characters || {};
  const page5 = analysis.page5_development || {};

  const downloadPDF = async () => {
    try {
      const response = await fetch(`/api/pdf?scriptId=${scriptId}`);
      const blob = await response.blob();
      const url = window.URL.createObjectURL(blob);
      const a = document.createElement('a');
      a.href = url;

      // Use script title from analysis (sanitize for filename)
      const scriptTitle = page1.title || 'Script';
      const sanitizedTitle = scriptTitle
        .replace(/[<>:"/\\|?*]/g, '') // Remove invalid filesystem characters
        .replace(/\s+/g, ' ') // Normalize spaces
        .trim();

      a.download = `ScriptScope - ${sanitizedTitle}.pdf`;
      document.body.appendChild(a);
      a.click();
      window.URL.revokeObjectURL(url);
      document.body.removeChild(a);
    } catch (error) {
      console.error('Failed to download PDF:', error);
      alert('Failed to download PDF. Please try again.');
    }
  };

  // Determine grade color
  const getGradeColor = (grade: string) => {
    if (!grade) return 'bg-gray-100 text-gray-800';
    const letter = grade.charAt(0);
    if (letter === 'A') return 'bg-green-100 text-green-800';
    if (letter === 'B') return 'bg-blue-100 text-blue-800';
    if (letter === 'C') return 'bg-yellow-100 text-yellow-800';
    if (letter === 'D') return 'bg-orange-100 text-orange-800';
    return 'bg-red-100 text-red-800';
  };

  return (
    <div className="w-full max-w-4xl mx-auto">
      {/* AI Disclaimer */}
      <div className="mb-4 text-xs text-gray-500 text-center">
        Analysis generated by AI · For informational purposes only ·{' '}
        <a href="/terms" className="underline hover:text-gray-700">
          Terms
        </a>
      </div>

      {/* Download Button */}
      <div className="text-center mb-8">
        <button
          onClick={downloadPDF}
          className="bg-[#1E3A5F] text-white px-8 py-3 rounded-lg font-medium hover:bg-[#152d47] transition-colors"
        >
          Download Full 5-Page Report (PDF)
        </button>
      </div>

      {/* Results Preview */}
      <div className="bg-white rounded-lg shadow-md p-8 space-y-8">
        {/* Grade & Score */}
        <div className="flex items-center justify-center gap-6">
          {page1.overallGrade && (
            <div className={`px-8 py-4 rounded-lg font-bold text-3xl ${getGradeColor(page1.overallGrade)}`}>
              {page1.overallGrade}
            </div>
          )}
          {page1.numericScore && (
            <div className="text-4xl font-bold text-[#c9a962]">
              {page1.numericScore}/10
            </div>
          )}
        </div>

        {/* Logline */}
        {page1.logline && (
          <div>
            <h2 className="text-2xl font-bold text-[#1E3A5F] mb-2">Logline</h2>
            <p className="text-gray-700 italic text-lg">{page1.logline}</p>
          </div>
        )}

        {/* Editorial Assessment */}
        {page1.editorialAssessment && (
          <div>
            <h2 className="text-2xl font-bold text-[#1E3A5F] mb-2">Editorial Assessment</h2>
            <p className="text-gray-700 text-lg leading-relaxed">{page1.editorialAssessment}</p>
          </div>
        )}

        {/* Reader Notes from Page 1 */}
        {page1.readerNotes && page1.readerNotes.length > 0 && (
          <div className="bg-amber-50 border-l-4 border-amber-400 p-4">
            <h3 className="text-lg font-bold text-amber-900 mb-2">📝 Reader Notes</h3>
            <ul className="space-y-2">
              {page1.readerNotes.map((note: string, idx: number) => (
                <li key={idx} className="text-gray-700 text-sm">• {note}</li>
              ))}
            </ul>
          </div>
        )}

        {/* Synopsis (shorter now) */}
        {page1.synopsis && (
          <div>
            <h3 className="text-xl font-bold text-[#1E3A5F] mb-2">Synopsis</h3>
            <p className="text-gray-700">{page1.synopsis}</p>
          </div>
        )}

        {/* Classification */}
        <div>
          <h3 className="text-xl font-bold text-[#1E3A5F] mb-2">Classification</h3>
          <div className="grid grid-cols-2 gap-4 text-gray-700">
            {page1.format && (
              <div>
                <span className="font-semibold">Format:</span> {page1.format}
              </div>
            )}
            {page1.genre && (
              <div>
                <span className="font-semibold">Genre:</span> {page1.genre}
              </div>
            )}
            {page1.pageCount && (
              <div>
                <span className="font-semibold">Pages:</span> {page1.pageCount}
              </div>
            )}
          </div>
        </div>

        {/* Protagonist Highlight */}
        {page2.protagonist?.name && (
          <div>
            <h3 className="text-xl font-bold text-[#1E3A5F] mb-2">Protagonist</h3>
            <div className="text-gray-700 space-y-1">
              <p><span className="font-semibold">Name:</span> {page2.protagonist.name}</p>
              <p><span className="font-semibold">Goal:</span> {page2.protagonist.goal}</p>
              <p><span className="font-semibold">Flaw:</span> {page2.protagonist.flaw}</p>
              {page2.protagonist.arcAssessment && (
                <div className="mt-2 pt-2 border-t">
                  <p className="text-sm italic">{page2.protagonist.arcAssessment}</p>
                </div>
              )}
            </div>
          </div>
        )}

        {/* Development Notes Preview */}
        {page5 && (
          <div>
            <h2 className="text-2xl font-bold text-[#1E3A5F] mb-4">Development Notes</h2>

            {/* What's Working */}
            {page5.whatsWorking?.length > 0 && (
              <div className="mb-6">
                <h3 className="text-xl font-bold text-green-700 mb-3">✓ What's Working</h3>
                <ul className="space-y-2">
                  {page5.whatsWorking.slice(0, 3).map((item: string, idx: number) => (
                    <li key={idx} className="text-gray-700 pl-4 border-l-4 border-green-500">
                      {item}
                    </li>
                  ))}
                  {page5.whatsWorking.length > 3 && (
                    <li className="text-gray-500 italic pl-4">
                      + {page5.whatsWorking.length - 3} more in full report
                    </li>
                  )}
                </ul>
              </div>
            )}

            {/* What Needs Work */}
            {page5.whatNeedsWork?.length > 0 && (
              <div className="mb-6">
                <h3 className="text-xl font-bold text-orange-700 mb-3">⚠ What Needs Work</h3>
                <ul className="space-y-2">
                  {page5.whatNeedsWork.slice(0, 3).map((item: string, idx: number) => (
                    <li key={idx} className="text-gray-700 pl-4 border-l-4 border-orange-500">
                      {item}
                    </li>
                  ))}
                  {page5.whatNeedsWork.length > 3 && (
                    <li className="text-gray-500 italic pl-4">
                      + {page5.whatNeedsWork.length - 3} more in full report
                    </li>
                  )}
                </ul>
              </div>
            )}

            {/* Top 3 Priorities */}
            {page5.topThreeFixes?.length > 0 && (
              <div className="bg-yellow-50 border-2 border-yellow-400 rounded-lg p-4">
                <h3 className="text-xl font-bold text-yellow-900 mb-3">🎯 Top Priorities for Next Draft</h3>
                <ol className="space-y-3">
                  {page5.topThreeFixes.map((item: string, idx: number) => (
                    <li key={idx} className="text-gray-800 text-sm">
                      <span className="font-bold text-yellow-700">#{idx + 1}:</span> {item}
                    </li>
                  ))}
                </ol>
              </div>
            )}
          </div>
        )}

        <div className="text-center text-sm text-gray-500 pt-4 border-t">
          This is a preview. Download the full 5-page PDF report for complete analysis with embedded
          reader notes, character assessments, structure evaluation, and actionable development suggestions.
        </div>
      </div>
    </div>
  );
}
